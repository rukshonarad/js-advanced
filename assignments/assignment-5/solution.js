function addTwoNumber(num1, num2) {
    let result = num1 + num2;
    return `The result of adding is ${result}.`;
}
console.log(addTwoNumber(1, 2));

function subtractTwoNumber(num1, num2) {
    let result = num1 - num2;
    return `The result of subtracting is ${result}.`;
}
console.log(subtractTwoNumber(4, 2));

function multiplyTwoNumbers(num1, num2) {
    let result = num1 * num2;
    return `The result of multiplying is ${result}.`;
}
console.log(multiplyTwoNumbers(2, 2));

function divideTwoNumbers(num1, num2) {
    let result = num1 / num2;
    return `The result of dividingis ${result}.`;
}
console.log(divideTwoNumbers(6, 2));
